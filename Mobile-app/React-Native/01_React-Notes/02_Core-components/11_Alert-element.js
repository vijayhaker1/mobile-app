// What is Alert Component?
/*
  The `Alert` component is use to show a dialogue prompt with a custom message and a title with few optional llist of button components in which we can bind some action function to it.

  We will refer the `Alert` api documentation in the APIs section of react-native website to learn more about the props.

  There are some differences while using the `Alert` API component which can vary platform to platform, in iOS we can have as many `Button` components as we want, but in Android side we can have only three limited buttons. 
*/