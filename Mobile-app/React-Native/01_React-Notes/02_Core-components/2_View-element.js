// What is View component?

/*
  The `View` component is a fundamental core component in React Native that serves as a building block for creating user interface.

  It works as a container that supports layout using flexbox, styling, touch handling, and accessibility controls
  In web-development terms, the `View` element can be compared with `div` element.

  The `View` component is typically nested inside other views and can have zero or more children of any type.
*/