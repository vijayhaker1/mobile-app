// What is `ScrollView` component?
/*
  In React-Native, the `ScrollView` component is use to make views or a layout scrollable although it is akin to div tag in terms of web-development, but it still not suppports scrolling feature because it meant to make views static or fixed.

  How does `ScrollView` component handles scrolling functionality in the layout?
  The `ScrollView` component wraps the platform-specific scrolling functionality.
  However the `ScrollView` requires a bounded height to function properly.


  In summery View element doen't scroll automatically and react-native styling not supports `overflowX` for x-axis or y-axis styles.
*/