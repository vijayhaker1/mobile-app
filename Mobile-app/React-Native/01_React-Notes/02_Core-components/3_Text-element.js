// What is Text component?
/*
  A `Text` component in React-Native is use to display text content on the mobile screen, it is similar to the paragrraph tag in terms of web-development.
  
  The `Text` component also supports nesting just like <p> tag, since it's like <p> tag which is a block level element, and we can style this component as well as it supports touch events.

  Depending on the target platform, React-Native will translate this component to either a <UITextView> in iOS devices, or a <TextView> in Android OS, and for web it converts it into <p> tag.

  Text component is important for to display any text content on the mobile devices screen.
*/