// What is ActivityIndicator component?
/*
  THe `ActivityIndicator` component displays a circular loading indicator icon.

  It is used to indorm the user about an ongoing processes, such as loading an app, show loader while submitting a form or updating something in the UI.

  The `ActivityIndicator` component has 3 props: `size`, `color` and `animating`.
  
  By default the `size` is set to `small`, which we can change to `large` these two pre-define values are supported.

  The default color would be picked based on the platfrom, and we can set any color of our choice.

  The `animating` prop accepts a boolean value, when it's set to `true` the loading indicator would visible otherwise will be hidden.

*/