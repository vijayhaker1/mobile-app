// What is React Native?
/*
  1. React Native is an open-source framework for building native Android and iOS applications using React.

  2. We use JavaScript to access platform-specific APIs such as camera and push notifications etc...

  3. We use React components to define the appearance and behavior of our UI.

  4. Important thing to note: React itself is a library for building user interfaces, we use React with pair of React-DOM, when we want to build web-apps, and for native mobile apps we use React-Native.
*/