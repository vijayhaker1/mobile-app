// How to create a new expo project?
/*
  Just like we create normal react app we can do the same to create an expo app the only difference is we'll `expo` rather than `react`.

  For npm:
  `npx create-expo-app@latest "my-appName"`

  For yarn:
  `yarn dlx create-expo-app@latest "my-appName"`

  we can ommit the `@latest` option if we want to use stable release.

  `create-expo-app` is a command-line tool to create an expo project just like `create-react-app`
*/